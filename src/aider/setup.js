/**
 * Build .aiderignore content.
 */
function buildAiderignore(ctx, stacks) {
  const lines = [
    '# Files Aider should not edit',
    '# Uses .gitignore syntax',
    '',
    '# Dependencies',
    'node_modules/',
    '__pycache__/',
    '.venv/',
    'venv/',
    '',
    '# Build artifacts',
    'dist/',
    'build/',
    '.next/',
    '',
    '# Data and large files',
    '*.csv',
    '*.parquet',
    '*.sqlite',
    '*.db',
    '',
    '# Generated files',
    'package-lock.json',
    'yarn.lock',
    'pnpm-lock.yaml',
    'poetry.lock',
    '',
  ];

  return lines.join('\n');
}

/**
 * Build .gitignore additions for Aider artifacts.
 */
function buildGitignoreAdditions() {
  return [
    '',
    '# Aider artifacts',
    '.aider*',
    '.env',
    '',
  ].join('\n');
}

function buildAiderSetupFiles(ctx, stacks) {
  const files = [];

  files.push({
    path: '.aider.conf.yml',
    content: buildAiderConfYml(ctx, stacks),
    label: 'Aider config',
  });

  files.push({
    path: 'CONVENTIONS.md',
    content: buildConventionsMd(ctx, stacks),
    label: 'Convention file',
  });

  files.push({
    path: '.env',
    content: buildEnvTemplate(),
    label: 'Environment template',
  });

  files.push({
    path: '.aiderignore',
    content: buildAiderignore(ctx, stacks),
    label: 'Aider ignore file',
  });

  return files;
}

/**
 * Main setup entry point — detects stacks, writes files, logs activity.
 */
function setupAider(options = {}) {
  const dir = options.dir || process.cwd();
  const log = options.log || console.log;
  const dryRun = options.dryRun || false;

  const ctx = new AiderProjectContext(dir);
  const stacks = ctx.detectStacks(STACKS);

  const setupFiles = buildAiderSetupFiles(ctx, stacks);

  const writtenFiles = [];
  const preservedFiles = [];

  for (const file of setupFiles) {
    const fullPath = path.join(dir, file.path);
    const exists = fs.existsSync(fullPath);

    if (exists && !options.overwrite) {
      preservedFiles.push(file.path);
      continue;
    }

    if (!dryRun) {
      const dirName = path.dirname(fullPath);
      if (!fs.existsSync(dirName)) {
        fs.mkdirSync(dirName, { recursive: true });
      }
      fs.writeFileSync(fullPath, file.content, 'utf8');
    }

    writtenFiles.push(file.path);
  }

  // Handle .gitignore additions (append, don't overwrite)
  const gitignorePath = path.join(dir, '.gitignore');
  if (!dryRun) {
    const existing = fs.existsSync(gitignorePath) ? fs.readFileSync(gitignorePath, 'utf8') : '';
    if (!/\.aider/i.test(existing)) {
      fs.appendFileSync(gitignorePath, buildGitignoreAdditions(), 'utf8');
      writtenFiles.push('.gitignore (appended)');
    }
  }

  // Activity artifacts
  let rollbackArtifact = null;
  let activityArtifact = null;

  if (!dryRun && writtenFiles.length > 0) {
    rollbackArtifact = writeRollbackArtifact(dir, 'aider-setup', writtenFiles);
    activityArtifact = writeActivityArtifact(dir, 'aider-setup', {
      platform: 'aider',
      writtenFiles,
      preservedFiles,
      stacks: stacks.map(s => s.key),
    });
  }

  log('');
  log(`  \x1b[1mAider Setup\x1b[0m — powered by nerviq v${version}`);
  log('');
  log(`  \x1b[32m${writtenFiles.length} files created.\x1b[0m`);
  if (preservedFiles.length > 0) {
    log(`  \x1b[2m${preservedFiles.length} existing files preserved (not overwritten).\x1b[0m`);
  }
  if (rollbackArtifact) {
    log(`  Rollback: \x1b[1m${rollbackArtifact.relativePath}\x1b[0m`);
  }
  log('');
  log('  Run \x1b[1mnpx nerviq --platform aider\x1b[0m to audit your Aider setup.');
  log('');

  return {
    created: writtenFiles.length,
    skipped: preservedFiles.length,
    writtenFiles,
    preservedFiles,
    stacks,
    rollbackArtifact: rollbackArtifact ? rollbackArtifact.relativePath : null,
    activityArtifact: activityArtifact ? activityArtifact.relativePath : null,
  };
}

