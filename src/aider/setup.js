/**
 * Aider Setup — file generation for .aider.conf.yml, .env, conventions, .gitignore
 *
 * Aider setup families:
 * - .aider.conf.yml (main config)
 * - .env (API keys)
 * - CONVENTIONS.md (project conventions, explicitly passed via read:)
 * - .aiderignore (file exclusion)
 * - .gitignore additions (exclude .aider* artifacts)
 */

const fs = require('fs');
const path = require('path');
const { version } = require('../../package.json');
const { STACKS } = require('../techniques');
const { writeActivityArtifact, writeRollbackArtifact } = require('../activity');
const { AiderProjectContext } = require('./context');
const { recommendAiderMcpPacks } = require('./mcp-packs');

function detectScripts(ctx) {
  const pkg = ctx.jsonFile('package.json');
  if (!pkg || !pkg.scripts) return {};
  return pkg.scripts;
}

function detectProjectName(ctx) {
  const pkg = ctx.jsonFile('package.json');
  if (pkg && pkg.name) return pkg.name;
  return path.basename(ctx.dir);
}

function hasStack(stacks, key) {
  return stacks.some((item) => item.key === key);
}

function buildLintCmd(ctx, stacks) {
  const scripts = detectScripts(ctx);
  if (scripts.lint) return 'npm run lint';
  if (hasStack(stacks, 'python') || hasStack(stacks, 'fastapi') || hasStack(stacks, 'django')) return 'ruff check --fix .';
  if (hasStack(stacks, 'go')) return 'golangci-lint run';
  if (hasStack(stacks, 'rust')) return 'cargo clippy --fix --allow-dirty';
  return null;
}

function buildTestCmd(ctx, stacks) {
  const scripts = detectScripts(ctx);
  if (scripts.test) return 'npm test';
  if (hasStack(stacks, 'python') || hasStack(stacks, 'fastapi') || hasStack(stacks, 'django')) return 'pytest';
  if (hasStack(stacks, 'go')) return 'go test ./...';
  if (hasStack(stacks, 'rust')) return 'cargo test';
  return null;
}

function buildMermaid(stacks) {
  if (hasStack(stacks, 'nextjs')) {
    return [
      '```mermaid',
      'graph TD',
      '    UI[App Router / Pages] --> Logic[Server Actions or API Routes]',
      '    Logic --> Data[Data Layer]',
      '    Data --> External[External Services / DB]',
      '```',
    ].join('\n');
  }

  if (hasStack(stacks, 'fastapi') || hasStack(stacks, 'django') || hasStack(stacks, 'python')) {
    return [
      '```mermaid',
      'graph TD',
      '    API[API / CLI Entry] --> Services[Service Layer]',
      '    Services --> Models[Models / Schemas]',
      '    Models --> Data[Database or External APIs]',
      '```',
    ].join('\n');
  }

  if (hasStack(stacks, 'go')) {
    return [
      '```mermaid',
      'graph TD',
      '    Cmd[cmd/ or main package] --> Internal[internal/ packages]',
      '    Internal --> Data[Storage / APIs]',
      '```',
    ].join('\n');
  }

  return [
    '```mermaid',
    'graph TD',
    '    Entry[Entry Point] --> Core[Core Logic]',
    '    Core --> Data[Data / External Services]',
    '```',
  ].join('\n');
}

/**
 * Build .aider.conf.yml content.
 */
function buildAiderConfYml(ctx, stacks) {
  const name = detectProjectName(ctx);
  const lintCmd = buildLintCmd(ctx, stacks);
  const testCmd = buildTestCmd(ctx, stacks);

  const lines = [
    `# Aider configuration for ${name}`,
    `# Generated by nerviq v${version}`,
    `# Precedence: env vars > CLI args > this file > defaults`,
    '',
    '# --- Model Configuration ---',
    '# model: claude-sonnet-4-20250514',
    '# editor-model: claude-sonnet-4-20250514',
    '# weak-model: gpt-4o-mini',
    '',
    '# --- Architect Mode (2-model workflow) ---',
    '# architect: true',
    '',
    '# --- Git Safety ---',
    'auto-commits: true',
    'dirty-commits: true',
    'attribute-author: true',
    'attribute-committer: true',
    'aider-commit-prefix: "aider: "',
    '',
    '# --- Verification Loop ---',
  ];

  if (lintCmd) {
    lines.push(`lint-cmd: "${lintCmd}"`);
    lines.push('auto-lint: true');
  } else {
    lines.push('# lint-cmd: "your lint command"');
    lines.push('# auto-lint: true');
  }

  if (testCmd) {
    lines.push(`test-cmd: "${testCmd}"`);
    lines.push('auto-test: true');
  } else {
    lines.push('# test-cmd: "your test command"');
    lines.push('# auto-test: true');
  }

  lines.push('');
  lines.push('# --- Context ---');
  lines.push('read:');
  lines.push('  - CONVENTIONS.md');
  lines.push('');
  lines.push('# --- Performance ---');
  lines.push('cache-prompts: true');
  lines.push('# map-tokens: 2048');
  lines.push('');
  lines.push('# --- Display ---');
  lines.push('show-diffs: true');
  lines.push('pretty: true');
  lines.push('# dark-mode: true');
  lines.push('');

  return lines.join('\n');
}

/**
 * Build CONVENTIONS.md content for Aider.
 */
function buildConventionsMd(ctx, stacks) {
  const name = detectProjectName(ctx);
  const mermaid = buildMermaid(stacks);
  const lintCmd = buildLintCmd(ctx, stacks);
  const testCmd = buildTestCmd(ctx, stacks);

  const lines = [
    `# ${name} — Project Conventions`,
    '',
    '## Architecture',
    '',
    mermaid,
    '',
    '## Verification Commands',
    '',
  ];

  if (testCmd) lines.push(`- Test: \`${testCmd}\``);
  if (lintCmd) lines.push(`- Lint: \`${lintCmd}\``);

  const scripts = detectScripts(ctx);
  if (scripts.build) lines.push(`- Build: \`npm run build\``);

  lines.push('');
  lines.push('## Coding Standards');
  lines.push('');
  lines.push('- Follow existing code style and patterns');
  lines.push('- Add tests for new functionality');
  lines.push('- Keep functions small and focused');
  lines.push('- Use descriptive variable and function names');
  lines.push('');
  lines.push('## Error Handling');
  lines.push('');
  lines.push('- Handle errors explicitly, do not swallow exceptions');
  lines.push('- Return meaningful error messages');
  lines.push('');
  lines.push('## Git Workflow');
  lines.push('');
  lines.push('- Aider auto-commits changes (git is the undo mechanism)');
  lines.push('- Use `/undo` to revert the last Aider change');
  lines.push('- Use `/run` to execute shell commands');
  lines.push('- Use `/test` to run tests');
  lines.push('- Use `/add` and `/drop` to manage files in context');
  lines.push('');

  return lines.join('\n');
}

